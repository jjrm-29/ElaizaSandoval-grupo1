    private void cargarDatosTabla() {
        List<Turno> turno = turnoControlador.leerTurnos();
        if (turno != null) {
            DefaultTableModel model = (DefaultTableModel) tablaTurno.getModel();
            model.setRowCount(0);
            for (Turno tur : turno) {
                Object[] row = {
                    tur.getID_Turno(),
                    tur.getDescripcion()
                };
                model.addRow(row);
            }
        }
    }

