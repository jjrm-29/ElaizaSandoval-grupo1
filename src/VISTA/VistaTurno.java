    private void accionBotonGuardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonGuardar
        String Descrepcion = textDescripcion.getText();

        if (!Descrepcion.isEmpty()) {

            turnoControlador.crearTurno(Descrepcion);
            cargarDatosTabla();

            textDescripcion.setText("");

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_accionBotonGuardar

    private void accionBotonEliminar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonEliminar
        int filaSeleccionada = tablaTurno.getSelectedRow();
        if (filaSeleccionada != -1) {
            int ID_Turno = (int) tablaTurno.getValueAt(filaSeleccionada, 0);
            turnoControlador.eliminarTurno(ID_Turno);
            cargarDatosTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.", "error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_accionBotonEliminar

    private void textBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textBuscarKeyTyped
        String textoBusqueda = textBuscar.getText().trim().toLowerCase();
        List<Turno> turno = turnoControlador.leerTurnos();

        DefaultTableModel modelo = (DefaultTableModel) tablaTurno.getModel();
        modelo.setRowCount(0);

        if (turno != null) {
            for (Turno tur : turno) {
                if (textoBusqueda.isEmpty()
                        || tur.getDescripcion().toLowerCase().contains(textoBusqueda)) {

                    Object[] fila = {
                        tur.getID_Turno(),
                        tur.getDescripcion()
                    };
                    modelo.addRow(fila);
                }
            }
        }
    }//GEN-LAST:event_textBuscarKeyTyped

    private void accionBotonActualizar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonActualizar
        String Descrepcion = textDescripcion.getText();

        if (!Descrepcion.isEmpty()) {

            turnoControlador.actualizarTurno(1, Descrepcion);
            cargarDatosTabla();

            textDescripcion.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }    }//GEN-LAST:event_accionBotonActualizar

    private void accionBotonLimpiar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonLimpiar
        Limpiar();
    }//GEN-LAST:event_accionBotonLimpiar

    private void tablaTurnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaTurnoMouseClicked
        if (evt.getClickCount() == 2) {

            int filaSeleccionada = tablaTurno.getSelectedRow();

            if (filaSeleccionada != -1) {

                TurnooSeleccionado = (int) tablaTurno.getValueAt(filaSeleccionada, 0);
                String Descripcion = (String) tablaTurno.getValueAt(filaSeleccionada, 1);

                textDescripcion.setText(Descripcion);

                btnEliminar.setEnabled(false);
                btnGuardar.setEnabled(false);

            }
        }
    }//GEN-LAST:event_tablaTurnoMouseClicked


