    public void cargarDatosTabla() {

        List<Profesor> profesor = profesorControlador.ObtnerTodosProfesores();

        if (profesor != null) {
            DefaultTableModel model = (DefaultTableModel) tablaProfesor.getModel();
            model.setRowCount(0);

            for (Profesor prof : profesor) {
                Object[] row = {
                    prof.getID_Profesor(),
                    prof.getNombre1(),
                    prof.getNombre2(),
                    prof.getApellido1(),
                    prof.getApellido2(),
                    prof.getCedula(),
                    prof.getTelefono()
                };
                model.addRow(row);
            }
        }
    }

