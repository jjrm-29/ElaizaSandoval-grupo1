    private void textBuscarcampotextoBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textBuscarcampotextoBuscarKeyTyped
        String textoBusqueda = textBuscar.getText().trim().toLowerCase();
        List<Profesor> profesor = profesorControlador.ObtnerTodosProfesores();

        DefaultTableModel modelo = (DefaultTableModel) tablaProfesor.getModel();
        modelo.setRowCount(0);

        if (profesor != null) {
            for (Profesor prof : profesor) {
                if (textoBusqueda.isEmpty()
                        || prof.getNombre1().toLowerCase().contains(textoBusqueda)
                        || prof.getNombre2().toLowerCase().contains(textoBusqueda)
                        || prof.getApellido1().toLowerCase().contains(textoBusqueda)
                        || prof.getApellido2().toLowerCase().contains(textoBusqueda)
                        || prof.getCedula().toLowerCase().contains(textoBusqueda)
                        || prof.getTelefono().toLowerCase().contains(textoBusqueda)) {

                    Object[] fila = {
                        prof.getID_Profesor(),
                        prof.getNombre1(),
                        prof.getNombre2(),
                        prof.getApellido1(),
                        prof.getApellido2(),
                        prof.getCedula(),
                        prof.getTelefono()
                    };
                    modelo.addRow(fila);
                }
            }
        }
    }//GEN-LAST:event_textBuscarcampotextoBuscarKeyTyped

    private void tablaProfesorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProfesorMouseClicked
        if (evt.getClickCount() == 2) {

            int filaSeleccionada = tablaProfesor.getSelectedRow();

            if (filaSeleccionada != -1) {

                ProfesorSeleccionada = (int) tablaProfesor.getValueAt(filaSeleccionada, 0);
                String Nombre1 = (String) tablaProfesor.getValueAt(filaSeleccionada, 1);
                String Nombre2 = (String) tablaProfesor.getValueAt(filaSeleccionada, 2);
                String Apellido1 = (String) tablaProfesor.getValueAt(filaSeleccionada, 3);
                String Apellido2 = (String) tablaProfesor.getValueAt(filaSeleccionada, 4);
                String Cedula = (String) tablaProfesor.getValueAt(filaSeleccionada, 5);
                String Telefono = (String) tablaProfesor.getValueAt(filaSeleccionada, 6);

                textNombre1.setText(Nombre1);
                textNombre2.setText(Nombre2);
                textApellido1.setText(Apellido1);
                textApellido2.setText(Apellido2);
                textCedula.setText(Cedula);
                textTelefono.setText(Telefono);

                btnEliminar.setEnabled(false);
                btnGuardar.setEnabled(false);

            }
        }
    }//GEN-LAST:event_tablaProfesorMouseClicked

    private void btnGuardaraccionBotonGuardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardaraccionBotonGuardar
        String Nombre1 = textNombre1.getText();
        String Nombre2 = textNombre2.getText();
        String Apellido1 = textApellido1.getText();
        String Apellido2 = textApellido2.getText();
        String Cedula = textCedula.getText();
        String Telefono = textTelefono.getText();

        if (!Nombre1.isEmpty() && !Nombre2.isEmpty() && !Apellido1.isEmpty() && !Apellido2.isEmpty() && !Cedula.isEmpty()
                && !Telefono.isEmpty()) {
            profesorControlador.crearProfesor(Nombre1, Nombre2, Apellido1, Apellido2, Cedula, Telefono);
            cargarDatosTabla();

            textNombre1.setText("");
            textNombre2.setText("");
            textApellido1.setText("");
            textApellido2.setText("");
            textCedula.setText("");
            textTelefono.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardaraccionBotonGuardar

    private void btnEliminaraccionBotonEliminar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminaraccionBotonEliminar
        int filaSeleccionada = tablaProfesor.getSelectedRow();
        if (filaSeleccionada != -1) {
            int ID_Profesor = (int) tablaProfesor.getValueAt(filaSeleccionada, 0);
            profesorControlador.eliminarProfesor(ID_Profesor);
            cargarDatosTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.", "error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminaraccionBotonEliminar

