    private void textBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textBuscarKeyTyped
        String textoBusqueda = textBuscar.getText().trim().toLowerCase();
        List<Grado> Grados = gradoControlador.leerTodosGrados();

        DefaultTableModel modelo = (DefaultTableModel) tablaGrado.getModel();
        modelo.setRowCount(0);

        if (Grados != null) {
            for (Grado Gra : Grados) {
                if (textoBusqueda.isEmpty()
                        || Gra.getDescripcion().toLowerCase().contains(textoBusqueda)
                        || Gra.getSeccion().toLowerCase().contains(textoBusqueda)) {

                    Object[] fila = {
                        Gra.getID_Grado(),
                        Gra.getDescripcion(),
                        Gra.getSeccion()
                    };
                    modelo.addRow(fila);
                }
            }
        }
    }//GEN-LAST:event_textBuscarKeyTyped

    private void tablaGradoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaGradoMouseClicked
        if (evt.getClickCount() == 2) {

            int filaSeleccionada = tablaGrado.getSelectedRow();

            if (filaSeleccionada != -1) {

                GradoSeleccionado = (int) tablaGrado.getValueAt(filaSeleccionada, 0);
                String Descripcion = (String) tablaGrado.getValueAt(filaSeleccionada, 1);
                String Seccion = (String) tablaGrado.getValueAt(filaSeleccionada, 2);

                textDescripcion.setText(Descripcion);
                textSeccion.setText(Seccion);

                btnEliminar.setEnabled(false);
                btnGuardar.setEnabled(false);

            }
        }
    }//GEN-LAST:event_tablaGradoMouseClicked


