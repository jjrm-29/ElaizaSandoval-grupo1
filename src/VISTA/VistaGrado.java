    private void cargarDatosTabla() {
        List<Grado> Grados = gradoControlador.leerTodosGrados();
        if (Grados != null) {
            DefaultTableModel model = (DefaultTableModel) tablaGrado.getModel();
            model.setRowCount(0);
            for (Grado Grad : Grados) {
                Object[] row = {
                    Grad.getID_Grado(),
                    Grad.getDescripcion(),
                    Grad.getSeccion()
                };
                model.addRow(row);
            }
        }
    }

