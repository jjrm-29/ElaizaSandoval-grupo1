    private void accionBotonEliminar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonEliminar
        int filaSeleccionada = tablaMatricula.getSelectedRow();
        if (filaSeleccionada != -1) {
            int idMatricula = (int) tablaMatricula.getValueAt(filaSeleccionada, 0);
            matriculaControlador.eliminarMatricula(idMatricula);
            cargarDatosTabla();

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar. ", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_accionBotonEliminar

    private void accionBotonGuardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonGuardar
        Date fecha = this.selectorfechaMatricula.getDate();
        java.sql.Date Fecha_Mat = new java.sql.Date(fecha.getTime());

        String ID_Estudiante = textEstudiante.getText();
        String ID_Profesor = textProfesor.getText();
        String ID_Turno = textTurno.getText();
        String ID_Grado = textGrado.getText();

        if (!ID_Estudiante.isEmpty() && !ID_Profesor.isEmpty() && !ID_Turno.isEmpty() && !ID_Grado.isEmpty()) {
            try {
                matriculaControlador.crearMatricula(8, 8, 1, 2, fecha);
                Limpiar();
                cargarDatosTabla();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Llene los campos requeridos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_accionBotonGuardar

    private void accionBotonActualizar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonActualizar
        String ID_Estudiante = textEstudiante.getText();
        String ID_Profesor = textProfesor.getText();
        String ID_Turno = textTurno.getText();
        String ID_Grado = textGrado.getText();
        Date fecha = this.selectorfechaMatricula.getDate();
        java.sql.Date Fecha_Mat = new java.sql.Date(fecha.getTime());

        if (!ID_Estudiante.isEmpty() && !ID_Profesor.isEmpty() && !ID_Turno.isEmpty() && !ID_Grado.isEmpty()) {

            matriculaControlador.actualizarMatricula(1, 2, 3, 4, 5, Fecha_Mat);
            cargarDatosTabla();

            textEstudiante.setText(ID_Estudiante);
            textProfesor.setText(ID_Profesor);
            textTurno.setText(ID_Turno);
            textGrado.setText(ID_Grado);

            btnEliminar.setEnabled(true);
            btnGuardar.setEnabled(true);

        } else {

            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_accionBotonActualizar

