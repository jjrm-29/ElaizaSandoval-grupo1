    private void cargarDatosTabla() {
        List<Matricula> matri = matriculaControlador.ObtnerTodosMatricula();
        if (matri != null) {
            DefaultTableModel model = (DefaultTableModel) tablaMatricula.getModel();
            model.setRowCount(0);
            for (Matricula tur : matri) {
                Object[] row = {
                    tur.getID_Matricula(),
                    tur.getID_Estudiante(),
                    tur.getID_Profesor(),
                    tur.getID_Grado(),
                    tur.getID_Turno(),
                    tur.getFecha_Mat()

                };
                model.addRow(row);
            }
        }
    }

